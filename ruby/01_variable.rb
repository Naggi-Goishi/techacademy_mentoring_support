# 変数

# 変数の定義
# nameという名前の変数を定義
name = "ゴイシ ナギ"

# ２行目で定義した変数をターミナルに表示
puts name # => ゴイシ ナギ

# 変数の種類
# 数値 ex. 1, 2, 3, 1.1, 1.2, -1
number = 1

# 文字列 ex. "Hello World", "こんにちは", "あ"
text   = "Hello World !!"

# シンボル ex. :snake, :hi, :ruby, :a
# シンボルは数値としてRubyの内部で変換されて処理します。
symbol = :ruby

# 配列 ex. ["りんご", "みかん", "メロン"], [1, 2, 3, 4, 5]
# 配列はリストとしていくつものデータを一つの変数として扱うことができます
fruits = ["りんご", "みかん", "メロン"]

